using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Calling_Method_Asssignment
{
    public class Calling_Method_Assignment
    {
        // Method 1: Squares the number
        public int Square(int number)
        {
            return number * number;
        }

        // Method 2: Doubles the number
        public int Double(int number)
        {
            return number * 2;
        }

        // Method 3: Subtracts 10 from the number
        public int SubtractTen(int number)
        {
            return number - 10;
        }
    }
}
